package grupCalısması;

public class WhileCarpımTablosu {

    public static void main(String[] args) {
/*kullanıcıdan  bır rakam alın ve bu rakam
     ıcın carpım tablosunu ekrana yazdırın*/

        int num = 10;
        int i = 1;
        while(i <= 10)
        {
            System.out.println( num+"*"+i +"="+ num * i);
            i++;
        }
    }
}

